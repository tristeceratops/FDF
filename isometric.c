#include "fdf.h"

t_dot	projection_iso(t_dot p)
{
	t_dot p2;

	p2.x = (p.x - p.y) * cos(0.42);
	p2.y = (p.x + p.y) * sin(0.42) - p.z;
	return (p2);
}